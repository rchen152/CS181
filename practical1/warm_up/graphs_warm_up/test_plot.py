import numpy as np

import matplotlib.pyplot as plt

# Create an array of 100 linearly-spaced points from 0 to 2*pi
x = np.zeros((20))
y = np.zeros((20))
for i in range(20):
    x[i] = i
    y[i] = np.sin(i)

# Create the plot
plt.plot(x,y)

# Save the figure in a separate file
plt.savefig('sine_function_plain.png')

# Draw the plot to the screen
plt.show()
